package who.wants.to.be.a.millionaire.aa.zw;

/**
 *
 * @author Adhis
 */
public class Player // save player data
{
    private final String name;
    private int score;

    public Player(String name) {
        this.name = name;
        this.score = 0;
    }

    public String getName() {
        return name;
    }

    public int getScore() {
        return score;
    }

    public void updateScore(int amount) 
    {
        score = amount;
    }
}
